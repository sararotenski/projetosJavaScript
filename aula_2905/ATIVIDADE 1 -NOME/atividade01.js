// Criar um programa que receba o nome, idade e endereço de uma pessoa e mostre na tela esses dados.

let nome = prompt('Digite o seu nome!')
var idade = prompt('Bem vindo(a) ' + nome + ', qual a sua idade?')
let endereço = prompt('Por último, qual o seu endereço?')
alert('Seu nome é ' + nome + ', você tem ' + idade + ' anos e seu endereço é ' + endereço +'!')